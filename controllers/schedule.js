import { StatusCodes } from 'http-status-codes'
import Schedule from '../models/schedule.js'
import validator from 'validator'

export const create = async (req, res) => {
  try {
    console.log('使用者:', req.user) // 這是 passport 設定的 req.user
    console.log('收到的請求資料:', req.body) // 這是前端表單傳來的資料，會在 req.body 中
    // 確保資料結構符合 Schema

    const data = {
      user: req.user._id,
      title: req.body.title || '',
      player: req.body.player || '',
      chara: req.body.chara || '',
      time: req.body.time || '',
      date: req.body.date || '',
    }

    console.log('存入的資料:', data)

    // 存入 MongoDB
    const result = await Schedule.create(data)

    res.status(200).json({
      success: true,
      message: '資料新增成功',
      result,
    })
  } catch (error) {
    // console.log(error)
    if (error.name === 'ValidationError') {
      const key = Object.keys(error.errors)[0]
      res.status(StatusCodes.BAD_REQUEST).json({
        success: false,
        message: error.errors[key].message,
      })
    } else {
      res.status(StatusCodes.INTERNAL_SERVER_ERROR).json({
        success: false,
        message: 'serverError',
      })
    }
  }
}

export const get = async (req, res) => {
  try {
    // 檢查 req.user 是否存在
    if (!req.user || !req.user._id) {
      return res.status(StatusCodes.UNAUTHORIZED).json({
        success: false,
        message: 'Unauthorized: User not authenticated',
      })
    }

    // 使用 user._id 查詢 資料
    const result = await Schedule.find({ user: req.user._id })
    res.status(StatusCodes.OK).json({
      success: true,
      message: '',
      result,
    })
  } catch (error) {
    console.log(error)
    res.status(StatusCodes.INTERNAL_SERVER_ERROR).json({
      success: false,
      message: 'serverError',
    })
  }
}

